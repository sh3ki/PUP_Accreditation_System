<h2 class="text-2xl/6 font-semibold text-maroon-700">History</h2>


<p class="text-base/8 text-left mt-4 font-light">
    The Polytechnic University of the Philippines (PUP) Calauan Campus started in 2009 by virtue of a Memorandum of
    Agreement between the Local Government of Calauan and PUP. On June 16, 2009, the doors of the campus opened to the
    youth of Calauan welcoming its first set of students.
</p>
<br>
<p class="text-base/8 text-left mt-4 font-light">
    With two course offerings: Bachelor in Business Teacher Education and Bachelor of Science in Entrepreneurial
    Management, PUP Calauan commenced with 159 students, 7 faculty members, and two administrative staff. The growing
    awareness of the public on the existence of PUP Calauan raised its enrolllment on its second year of operation. The
    number of students rose from 159 to 275. This also brought a higher need for more qualified faculty and staff.
</p>
<br>
<p class="text-base/8 text-left mt-4 font-light">
    PUP Calauan takes pride of its various contributions to the community. Its students and administration has been in
    constant support with the local government unit in all its undertakings. It has launched various activities which
    involved different sectors of the town. It has established linkages with the people in different villages through
    various community extension programs.
</p>
<br>

<p class="text-base/8 text-left mt-4 font-light">
    At present, PUP Calauan houses 520 students, 25 faculty members, and three administrative staff. It is indeed
    remarkable that by the end of the School Year 2012-2013, the campus will have its first batch of graduates for the
    BBTE and BSEM courses. The campus continues to live up to its mission of providing quality education to the youth as
    it prepares its Education students for the Board Examination this August 2013. Likewise, the local government has
    found an ally in its thrust of making Calauan a conducive environment for growth and development.

</p>
