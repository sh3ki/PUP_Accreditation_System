<h2 class="text-2xl/6 font-semibold text-maroon-700">PUP Calauan, Laguna Campus</h2>

<p class="text-base/8 text-left mt-4 font-light">
    Instituted in 2009 by virtue of a Memorandum of Agreement between the Local Government of Calauan and the
    Polytechnic University of the Philippines, PUP Calauan is one of the four PUP Campuses in the province of Laguna
    that offers two undergraduate programs: Bachelor in Business Teacher Education Major in Business Technology and
    Livelihood Education, and Bachelor of Science in Entrepreneurship. It houses more than 500 students, most of which
    are local residents of the town.
</p>

